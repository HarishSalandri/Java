package Loop;

public class While {
public static void main(String[] args) {
	//print the values from 2 to 200 using while loop
	int num=2;
	while(num<=200) {
		System.out.println(num);
		num++;
	}
	System.out.println("  ");
	
	//print the values from 8 to 800 using while loop
	int n=8;
	while(n<=800) {
		System.out.println(n);
		n++;
	}
}
}
